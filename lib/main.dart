    print("THE LOCALE>>$locale");
    return Sizer(
      builder: (context, orientation, deviceType) {
        SystemChrome.setSystemUIOverlayStyle(
          SystemUiOverlayStyle(statusBarColor: ThemeProvider.primary),
        );
        return GetMaterialApp(
          title: 'Scouttalent',
          debugShowCheckedModeBanner: false,
          navigatorKey: Get.key,
          translations: AppTranslation(),

          initialRoute: AppRouter.splash,
          getPages: AppRouter.routes,
          defaultTransition: Transition.fadeIn,
          locale: Locale(locale),
          fallbackLocale: const Locale('en'),
          builder: (context, child) {
            return Obx(() {
              final controller = Get.find<ConnectivityController>();
              return Stack(
                children: [
                  child ?? const SizedBox.shrink(),
                  if (!controller.isConnected) const NoConnectivityScreen(),
                ],
              );
            });
          },
        );
      },
    );
  }
}

Future<String> getSavedLocale() async {
  SharedPreferences prefs = await SharedPreferences.getInstance();
  String? langCode = prefs.getString(AppString.currentLang);
  return langCode ?? 'en';
}
